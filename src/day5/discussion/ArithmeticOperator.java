package day5.discussion;


public class ArithmeticOperator {
    // + - * / %
    public static void main(String[] args) {
        int num = 55;
        int num2 = 10;
        int sum = num + num2;
        
        System.out.println("Sum: " + sum);
        System.out.println(num - num2);
        System.out.println(num * num2);
        System.out.println(num / num2);
        System.out.println(num % num2);
    }
}
